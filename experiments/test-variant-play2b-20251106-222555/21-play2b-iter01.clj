(ns examples.program21 (:require [clojure.string :as str]))
(defn split-words [s] (str/split s #"\s+"))
(defn reverse-words [s] (->> (split-words s) (map (fn [word] (str/reverse word)))))
(str/join " ")
(defn title-case [s] (->> (split-words s) (map (fn [word] (str (str/upper-case (subs word 0 1)) (subs word 1))))))
(str/join " ")