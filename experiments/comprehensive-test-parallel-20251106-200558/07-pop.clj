(ns examples.program7)
(defmulti render-shape :type)
(defmethod render-shape :circle [shape] (str "Circle with radius: " (:radius shape)))
(defmethod render-shape :rectangle [shape] (str "Rectangle with width: " (:width shape) " and height: " (:height shape)))
(defmethod render-shape :default [shape] (str "Error: Unknown shape type " (:type shape)))